class triangulo:
    
    def area(self) -> None:
        b = int(input("\nIngresa la base: "))
        h = int(input("Ingresa la altura: "))
        print("\nArea: ",((b*h)/2))


def perimtetro(self) -> None:
    l1 = int(input("\nIngrese el lado 1: "))
    l2 = int(input("Ingrese el lado 2: "))
    l3 = int(input("Ingrese el lado 3: "))
    print("\nPerimetro: ", (l1+l2+l3))